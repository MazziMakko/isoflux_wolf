/**
 * Generate location-specific content
 */
export function getLocationContent(
  baseContent: string,
  location: { city?: string; region?: string; country?: string }
): string {
  if (!location.city) return baseContent;

  const locationString = `${location.city}${location.region ? `, ${location.region}` : ''}${location.country ? `, ${location.country}` : ''}`;
  
  return `${baseContent} Proudly serving financial institutions in ${locationString}.`;
}

/**
 * Generate keywords with location
 */
export function getLocationKeywords(
  baseKeywords: string[],
  location: { city?: string; region?: string; country?: string }
): string[] {
  if (!location.city) return baseKeywords;

  const locationKeywords = baseKeywords.flatMap(keyword => [
    keyword,
    `${keyword} ${location.city}`,
    `${keyword} ${location.region || location.country}`,
  ]);

  return [...new Set(locationKeywords)]; // Remove duplicates
}
